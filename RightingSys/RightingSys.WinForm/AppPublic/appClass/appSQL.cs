                         when not matched by source and target.rolekey='{0}' then    delete    output $action ,inserted.RoleFunctionKey ,deleted.RoleFunctionKey;", guidRoleKey);


            using (SqlConnection conn = new SqlConnection(ConnectionString))
            {
                conn.Open();
